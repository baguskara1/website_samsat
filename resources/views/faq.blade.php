                <details class="faq-item" open>
                    <summary>Apakah ada layanan pembayaran pajak secara online?</summary>
                        <div class="faq-answer">
                        <p>Pembayaran pajak tahunan secara online dapat menggunakan aplikasi seperti aplikasi Samsat Digital Nasional (SIGNAL), Gotagihan Gojek, dan BPD DIY mobile banking.</p>
                        </div>
                </details>

                <details class="faq-item" open>
                    <summary>Apakah tersedia pembayaran nontunai?</summary>
                        <div class="faq-answer">
                        <p>Untuk pembayaran nontunai tersedia opsi dengan kartu mesin EDC (kecuali BCA), dan pembayaran dengan menggunakan QRIS yang dapat menerima pembayaran dari berbagai dompet digital maupun M Banking.</p>
                        </div>
                </details>

                <details class="faq-item" open>
                    <summary>Apakah kendaraan Luar DIY dapat membayar pajak di Samsat DIY?</summary>
                        <div class="faq-answer">
                        <p>Pembayaran Pajak kendaraan tidak bisa lintas provinsi. Pajak Kendaraan Bermotor hanya bisa dibayarkan di Kantor Samsat di wilayah provinsi yang sama.</p>
                        </div>
                </details>

                <details class="faq-item" open>
                    <summary>Apakah bisa membayar pajak 5 tahunan secara online?</summary>
                        <div class="faq-answer">
                        <p>Pembayaran pajak 5 tahunan tidak bisa dilakukan secara online, silakan datang ke kantor Samsat Sleman atau Samsat Maguwoharjo.Selain itu pembayaran 5 tahunan juga dilayani di Samsat Induk lainnya di DIY dengan catatan: Nama alamat pemilik di STNK dan KTP masih sesuai dan nomor polisi kendaraan tidak termasuk nomor pilihan.</p>
                        </div>
                </details>
                
                <details class="faq-item" open>
                    <summary>Syarat balik nama kendaraan bermotor apa saja?</summary>
                        <div class="faq-answer">
                        <p>Syarat balik nama adalah BPKB asli, STNK asli, KTP pemilik baru, kuitansi jual beli/risalah lelang/akta hibah/waris.</p>
                        </div>
                </details>

